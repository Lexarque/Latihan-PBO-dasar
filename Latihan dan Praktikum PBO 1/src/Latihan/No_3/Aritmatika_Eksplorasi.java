package Latihan.No_3;

public class Aritmatika_Eksplorasi {

    public static void main(String[] args) {

        // Deklarasi variabel dan tipe data

        float bil_a, bil_b, hasil;

        // Inisialisasi variabel di atas

        bil_a = 454.55f;
        bil_b = 546.55f;

        // Inisialisasi operator untuk menambahkan kedua variabel di atas

        hasil = bil_a + bil_b;

        // Menampilkan hasil operator
        // Hasilnya pun akan beda dengan Aritmatika yang sebelumnya karena tipe data nya juga berbeda

        System.out.print("Hasil penjumlahan kedua variabel : ");
        System.out.println(hasil);
    }
}
